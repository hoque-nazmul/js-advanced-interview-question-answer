let numOne = 2;
let numTwo = "2";
// == check only value but === check value and data type also.
if(numOne == numTwo) {
    console.log("Condition is true!");
}
else {
    console.log("Condition is false!");
}
// Output: condition is true

if(numOne === numTwo) {
    console.log("Condition is true!");
}
else {
    console.log("Condition is false!");
}
// Output: condition is false